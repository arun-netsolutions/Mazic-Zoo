      <p>Zoo Animal
National Zoological Park is home to a large number of species of birds, reptiles and mammals which live in an environment that in many ways resemble their natural habitat. The zoo not only provides a home for endangered species, but also helps them to breed well in captivity. Some of the special attractions at the Zoo include White Tiger, Gaur, Jaguar, Rhinoceros, Elephants, Brow-antlered Deer (Sangai), Lion-tailed Macaque, Migratory birds (during winters) etc.</p>
